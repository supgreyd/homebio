/**
 * Get SVG icon markup
 *
 * @param string $name Icon name
 * @param int    $size Icon size (width and height)
 * @param array  $attrs Additional attributes
 * @return string SVG markup
 */
function homebio_icon($name, $size = 20, $attrs = []) {
    $icons = homebio_get_icons();

    if (!isset($icons[$name])) {
        return '';
    }

    $default_attrs = [
        'xmlns'        => 'http://www.w3.org/2000/svg',
        'width'        => $size,
        'height'       => $size,
        'viewBox'      => '0 0 24 24',
        'fill'         => 'none',
        'stroke'       => 'currentColor',
        'stroke-width' => '2',
        'aria-hidden'  => 'true',
    ];

    $attrs = array_merge($default_attrs, $attrs);

    // Build attributes string
    $attrs_str = '';
    foreach ($attrs as $key => $value) {
        $attrs_str .= sprintf(' %s="%s"', esc_attr($key), esc_attr($value));
    }

    return sprintf('<svg%s>%s</svg>', $attrs_str, $icons[$name]);
}

/**
 * Echo SVG icon markup
 *
 * @param string $name Icon name
 * @param int    $size Icon size
 * @param array  $attrs Additional attributes
 */
function homebio_icon_e($name, $size = 20, $attrs = []) {
    echo homebio_icon($name, $size, $attrs);
}

