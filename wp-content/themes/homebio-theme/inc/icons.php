    $icon_map = [
        'price'    => 'euro',
        'area'     => 'maximize',
        'address'  => 'map-pin',
        'location' => 'map-pin',
        'bedrooms' => 'bed',
        'rooms'    => 'home',
        'info'     => 'info',
        'success'  => 'check-circle',
        'error'    => 'x-circle',
        'warning'  => 'alert-triangle',
    ];

    $icon_name = isset($icon_map[$type]) ? $icon_map[$type] : 'info';

    return homebio_icon($icon_name, $size);
}
